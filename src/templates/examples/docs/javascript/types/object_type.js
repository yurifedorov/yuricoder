let person = {   // объект "person"
  number: 1,     // ключ "number", значение 1
  name: "Mike",  // ключ "name", значение "Mike"
  age: 25        // ключ "age",  значение 25
};

/* обращение к свойствам объекта */
alert(person.number); // 1
alert(person.name);   // Mike
alert(person.age);    // 25